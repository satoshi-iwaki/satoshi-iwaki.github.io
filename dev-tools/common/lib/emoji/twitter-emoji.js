const Twemoji = {};

Twemoji.png = function(key) {
  const code = Twemoji.code[key];
  if (!code) {
    return;
  }
  return `./assets/emoji/png/${code.join('-')}.png`;
}

Twemoji.svg = function(key) {
  const code = Twemoji.code[key];
  if (!code) {
    return;
  }
  return `./assets/emoji/svg/${code.join('-')}.svg`;
}

Twemoji.image = function(key) {
  const image = Twemoji.png(key);
  if (!image) {
    return;
  }
  return `<img width="15" height="15" src="${image}" alt="icon" emoji="${key}" async></img>`
}

Twemoji.replace = function(markdown, loader) {
  let output = markdown;
  const emojis = markdown.match(/(:[a-z|A-Z|0-9|+|\-|_]*:)/g)
  if (emojis && emojis.length > 0) {
    emojis.forEach( function(value) {
      if (value.length < 3) {
        return;
      }
      const key = value.slice(1, -1);
      const path = Twemoji.svg(key)
      if (!path) {
        return;
      }
      const svg = loader(path)
      output = output.replace(value, `<emoji>${svg}</emoji>`);
    })
  }
  return output;
}


Twemoji.list = function(handler) {
  return Twemoji.keyList.map(key => handler(key.key, Twemoji.png(key.key)))
}

